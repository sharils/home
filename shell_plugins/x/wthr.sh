[ -n "$SET_X" ] && set -x

whtr() {
  cat <<'EOF'
          兒童         男性         女性
非常瘦    < 0.34       < 0.34       < 0.34
健康的瘦  0.35 - 0.45  0.35 - 0.42  0.35 - 0.41
健康      0.46 - 0.51  0.43 - 0.52  0.42 - 0.48
過重      0.52 - 0.63  0.53 - 0.57  0.49 - 0.53
嚴重超重  ≥ 0.64       0.58 - 0.62  0.54 - 0.57
病態肥胖               ≥ 0.63       ≥ 0.58
EOF

  echo

  printf 'H: %s, W: %s, WtHR: %s' "$1" "$2" "$(bc --mathlib --expression="scale=2; $2 / $1")"
}

whtr "$@"
